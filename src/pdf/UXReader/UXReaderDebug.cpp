void DBLog(const wchar_t *format, ...)
{
#ifdef _DEBUG // Log message
	wchar_t output[4096]; wchar_t text[4096];

	SYSTEMTIME lt; GetLocalTime(&lt); // Get local time

	va_list arguments; va_start(arguments, format); vswprintf_s(text, 4096, format, arguments); va_end(arguments);

	swprintf_s(output, 4096, L"[%4i-%02i-%02i %02i:%02i:%02i.%03i] %s", lt.wYear, lt.wMonth, lt.wDay, lt.wHour, lt.wMinute, lt.wSecond, lt.wMilliseconds, text);

	OutputDebugStringW(output);
#endif // _DEBUG
}

void WMLog(const HWND hWnd, const UINT message, const WPARAM wParam, const LPARAM lParam)
{
#ifdef _DEBUG // Log message
	if (message < (sizeof(wmMessageStrings) / sizeof(wchar_t *)))
		DBLog(L"HWND 0x%p - message %s - wParam 0x%08x - lParam 0x%08x\n", hWnd, wmMessageStrings[message], wParam, lParam);
	else
		DBLog(L"HWND 0x%p - message 0x%04x - wParam 0x%08x - lParam 0x%08x\n", hWnd, message, wParam, lParam);
#endif // _DEBUG
}

