void DBHex(const void *data, const size_t size)
{
#ifdef _DEBUG // Log hex dump

	if ((data != nullptr) && (size > 0)) // Hex dump data size
	{
		const unsigned char *ptr = reinterpret_cast<const unsigned char *>(data);

		size_t count = size; int offset = 0; const int bpr = 16; wchar_t text[128];

		std::wstring ws_ptr; std::wstring ws_hex; std::wstring ws_ascii;

		while (count > 0) // Hex dump loop
		{
			if (offset == 0) // Start new row
			{
				swprintf_s(text, 128, L"0x%p ", ptr); ws_ptr.assign(text);

				ws_hex.clear(); ws_ascii.clear();
			}

			const unsigned char byte = *ptr; // Get data byte

			swprintf_s(text, 128, L"%02X ", byte); ws_hex.append(text);

			ws_ascii.push_back(((byte >= 0x20) && (byte <= 0x7E)) ? byte : '.');

			count--; offset++; ptr++;

			if (offset >= bpr) // Output full row
			{
				DBLog(L"%s %s %s\n", ws_ptr.data(), ws_hex.data(), ws_ascii.data()); offset = 0;
			}
		}

		if (offset > 0) // Output final row
		{
			while (offset < bpr) { ws_hex.append(L"-- "); offset++; }

			DBLog(L"%s %s %s\n", ws_ptr.data(), ws_hex.data(), ws_ascii.data());
		}
	}
#endif // _DEBUG
}
